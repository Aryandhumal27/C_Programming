#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CheckNumberType
//  Discription :   It is used to check whether a number is positive, negative, or zero
//  Input :         int
//  Output :        void
//  Author :        Aryan Pandharinath Dhumal
//  Date :          18/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////

void CheckNumberType(int iNo)
{
    if(iNo == 0)
    {
        printf("%d is Zero", iNo);
    }
    else if(iNo > 0)
    {
        printf("%d is Positive", iNo);
    }
    else
    {
        printf("%d is Negative", iNo);
    }
}   //  End of function CheckNumberType

////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                         // Variable to store user input

    printf("Enter number : \n");
    scanf("%d", &iValue);                   // Accept user input

    CheckNumberType(iValue);                // Function call 

    return 0;
}   //  End of main

