//  5.Accept one number from user and print that number of * on screen

////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Required Header files
//
////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Accept
//  Discription :   It is used to print the number of * on the screen
//  Input :         int
//  Output :        void
//  Author :        Aryan Pandharinath Dhumal
//  Date :          17/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
    int iCnt = 0;                           // Loop counter variable

                                                
    for(iCnt = 1; iCnt <= iNo; iCnt++)      // Loop to print * iNo times
    {
        printf("*");                        // Print * on the screen
    }
}   //  End of function Accept

////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                         // Variable to store user input

    iValue = 5;                             

    Accept(iValue);                         // Method call 

    return 0;
}   //  End of main

