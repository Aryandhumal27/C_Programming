void Display()
{
    int iCnt = 0;                            // Counter variable for loop

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        printf("Marvellous\n");              // Business Logic
    }
}   //  End of function Display

////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    Display();                              //  Method call

    return 0;
}   //  End of main

