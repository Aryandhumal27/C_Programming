#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Discription :   It is used to print 5 to 1 numbers on screen.
//  Input :         
//  Output :        void
//  Author :        Aryan Pandharinath Dhumal
//  Date :          17/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt = 0;                               //  Counter variable for loop
    iCnt = 5;

    while(iCnt > 0)
    {
        printf("%d\t", iCnt);
        iCnt--;
    }
}   //  End of function Display

int main()
{
    Display();                                  //  Method Call

    return 0;
}   //  End of main

